class LogicPattern(TypedDict):
    approach_label: str
    approach_description: str
    persuasion_key: str
    structural_blueprint: NotRequired[str]
    evidence_strategy: NotRequired[str]
    transition_logic: NotRequired[str]


class StructureBlock(TypedDict, total=False):
    structure: str
    effect: str
    examples_summary: str


class LogicPatternsPayload(TypedDict):
    question_type: str
    human_reviewed: bool
    patterns: list[LogicPattern]
    section_balance: NotRequired[str]
    opening_pattern: NotRequired[StructureBlock]
    closing_pattern: NotRequired[StructureBlock]
    quality_markers: NotRequired[list[str]]
    common_weaknesses: NotRequired[list[str]]
    # 文字数帯(CharBand値)ごとの列挙テンプレ例。各列挙要素は句点で独立完結させ、
    # 読点で連結しない。enumeration が窮屈になる短帯や、列挙が不要な設問では省く。
    enumeration_phrasing: NotRequired[dict[str, list[str]]]


