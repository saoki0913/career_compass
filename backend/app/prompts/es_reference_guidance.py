def get_reference_guidance(question_type: str) -> QuestionTypeGuidance:
    guidance = QUESTION_TYPE_GUIDANCE.get(question_type)
    return guidance if isinstance(guidance, dict) else {}


def get_quality_hints(question_type: str) -> list[str]:
    hints = get_reference_guidance(question_type).get("quality_hints")
    if not isinstance(hints, list):
        return []
    return [item for item in hints if isinstance(item, str) and item.strip()]


def get_skeleton(question_type: str, *, char_max: int | None = None) -> list[str]:
    guidance = get_reference_guidance(question_type)
    if char_max is not None:
        bands = guidance.get("bands")
        if isinstance(bands, dict):
            band_skeleton = bands.get(select_char_band(char_max).value)
            if isinstance(band_skeleton, list) and band_skeleton:
                return [item for item in band_skeleton if isinstance(item, str) and item.strip()]
    skeleton = guidance.get("skeleton")
    if not isinstance(skeleton, list):
        return []
    return [item for item in skeleton if isinstance(item, str) and item.strip()]


def get_sentence_flow(question_type: str) -> dict[str, str]:
    flow = get_reference_guidance(question_type).get("sentence_flow")
    if not isinstance(flow, dict):
        return {}
    return {
        str(key): str(value)
        for key, value in flow.items()
        if isinstance(key, str) and isinstance(value, str) and value.strip()
    }


def get_logic_patterns_payload(question_type: str) -> dict[str, object] | None:
    payload = get_reference_guidance(question_type).get("logic_patterns")
    return payload if isinstance(payload, dict) else None


def _is_str_list(value: object) -> bool:
    return isinstance(value, list) and all(isinstance(item, str) for item in value)


def validate_guidance_entry(entry: object) -> bool:
    """Runtime structural check of one QuestionTypeGuidance (mypy is not configured).

    An empty dict is valid (not-yet-curated placeholder). Present keys must have
    the correct shape. Used by the contract test to guarantee type safety.
    """

    if not isinstance(entry, dict):
        return False
    if not entry:
        return True

    if "quality_hints" in entry and not _is_str_list(entry["quality_hints"]):
        return False
    if "skeleton" in entry and not _is_str_list(entry["skeleton"]):
        return False
    if "sentence_flow" in entry:
        flow = entry["sentence_flow"]
        if not isinstance(flow, dict) or not all(
            isinstance(k, str) and isinstance(v, str) for k, v in flow.items()
        ):
            return False
    if "bands" in entry:
        bands = entry["bands"]
        if not isinstance(bands, dict):
            return False
        valid_keys = {band.value for band in CharBand}
        for key, value in bands.items():
            if key not in valid_keys or not _is_str_list(value):
                return False
    if "logic_patterns" in entry:
        lp = entry["logic_patterns"]
        if not isinstance(lp, dict):
            return False
        if not isinstance(lp.get("question_type"), str) or not lp["question_type"].strip():
            return False
        if lp.get("human_reviewed") is not True:
            return False
        patterns = lp.get("patterns")
        if not isinstance(patterns, list) or not patterns:
            return False
        for pattern in patterns:
            if not isinstance(pattern, dict):
                return False
            for required in ("approach_label", "approach_description", "persuasion_key"):
                if not isinstance(pattern.get(required), str) or not pattern[required].strip():
                    return False
        for opt_list in ("quality_markers", "common_weaknesses"):
            if opt_list in lp and not _is_str_list(lp[opt_list]):
                return False
        for opt_block in ("opening_pattern", "closing_pattern"):
            if opt_block in lp and not isinstance(lp[opt_block], dict):
                return False
        if "enumeration_phrasing" in lp:
            phrasing = lp["enumeration_phrasing"]
            if not isinstance(phrasing, dict) or not phrasing:
                return False
            valid_band_keys = {band.value for band in CharBand}
            for band_key, items in phrasing.items():
                if band_key not in valid_band_keys or not _is_str_list(items) or not items:
                    return False
    return True
