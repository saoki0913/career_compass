class CharBand(StrEnum):
    """Character-count band keys. Mirrors the 6 bands in docs/reference/es-review/{type}.md."""

    LE_100 = "le_100"
    B_100_200 = "100_200"
    B_200_300 = "200_300"
    B_300_400 = "300_400"
    B_400_500 = "400_500"
    GE_500 = "ge_500"


DEFAULT_CHAR_BAND = CharBand.B_300_400

_BAND_LABELS: dict[CharBand, str] = {
    CharBand.LE_100: "〜100字",
    CharBand.B_100_200: "100〜200字",
    CharBand.B_200_300: "200〜300字",
    CharBand.B_300_400: "300〜400字",
    CharBand.B_400_500: "400〜500字",
    CharBand.GE_500: "500字以上",
}


def select_char_band(char_max: int | None) -> CharBand:
    """Resolve a character-count upper bound to its band. None -> default band."""

    if char_max is None:
        return DEFAULT_CHAR_BAND
    if char_max <= 100:
        return CharBand.LE_100
    if char_max <= 200:
        return CharBand.B_100_200
    if char_max <= 300:
        return CharBand.B_200_300
    if char_max <= 400:
        return CharBand.B_300_400
    if char_max <= 500:
        return CharBand.B_400_500
    return CharBand.GE_500


def char_band_label(band: CharBand) -> str:
    return _BAND_LABELS.get(band, _BAND_LABELS[DEFAULT_CHAR_BAND])


def _enumeration_for_long_bands(text: str) -> dict[str, list[str]]:
    """200字以上の4帯に同一の列挙テンプレ例を割り当てる（理由/目標の N 点分割型）。

    100字以下・100〜200字は理由/目標を2点に分けると窮屈になるため除外する。
    """

    return {
        CharBand.B_200_300.value: [text],
        CharBand.B_300_400.value: [text],
        CharBand.B_400_500.value: [text],
        CharBand.GE_500.value: [text],
    }


