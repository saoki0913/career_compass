#!/usr/bin/env node
import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";

const repoRoot = process.cwd();
const targetPath = path.resolve(repoRoot, ".next", "dev");
const expectedParent = path.resolve(repoRoot, ".next");
const args = new Set(process.argv.slice(2));
const dryRun = args.has("--dry-run");

function writeLine(message = "") {
  process.stdout.write(`${message}\n`);
}

function fail(message) {
  process.stderr.write(`${message}\n`);
  process.exit(1);
}

function run(command, args) {
  try {
    return execFileSync(command, args, {
      cwd: repoRoot,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "pipe"],
    }).trim();
  } catch (error) {
    const status =
      error && typeof error === "object" && "status" in error
        ? Number(error.status)
        : 1;
    if (status === 1) return "";
    return null;
  }
}

function inspectRunningNextDev() {
  const output = run("ps", ["-axo", "pid=,command="]);
  if (output === null) return null;
  if (!output) return false;

  const currentPid = String(process.pid);
  return output.split("\n").some((line) => {
    const trimmed = line.trim();
    if (!trimmed) return false;
    const firstSpace = trimmed.indexOf(" ");
    if (firstSpace === -1) return false;
    const pid = trimmed.slice(0, firstSpace);
    const command = trimmed.slice(firstSpace + 1);
    if (pid === currentPid) return false;
    return /\bnext-server\b/.test(command) || /\bnext\s+dev\b/.test(command);
  });
}

function printSize(label) {
  if (!fs.existsSync(targetPath)) {
    writeLine(`${label}: .next/dev not found`);
    return;
  }
  try {
    const output = execFileSync("du", ["-sh", targetPath], {
      cwd: repoRoot,
      encoding: "utf8",
      stdio: ["ignore", "pipe", "pipe"],
    }).trim();
    writeLine(`${label}: ${output}`);
  } catch (error) {
    const detail =
      error && typeof error === "object" && "message" in error
        ? String(error.message)
        : String(error);
    writeLine(`${label}: size unavailable (${detail})`);
  }
}

function assertSafeLocalContext() {
  if (process.env.CI === "1" && !dryRun) {
    fail("Refusing to remove .next/dev in CI. Run locally after stopping Next dev.");
  }

  if (process.env.NODE_ENV === "production" && !dryRun) {
    fail("Refusing to remove .next/dev with NODE_ENV=production.");
  }
}

function assertSafeTarget() {
  if (path.dirname(targetPath) !== expectedParent || path.basename(targetPath) !== "dev") {
    fail(`Refusing to remove unexpected path: ${targetPath}`);
  }

  const relativeTarget = path.relative(repoRoot, targetPath);
  if (relativeTarget.startsWith("..") || path.isAbsolute(relativeTarget)) {
    fail(`Refusing to remove path outside repo: ${targetPath}`);
  }

  if (fs.existsSync(expectedParent) && fs.lstatSync(expectedParent).isSymbolicLink()) {
    fail("Refusing to remove .next/dev because .next is a symlink.");
  }

  if (fs.existsSync(targetPath)) {
    const stat = fs.lstatSync(targetPath);
    if (stat.isSymbolicLink()) {
      fail("Refusing to remove .next/dev because it is a symlink.");
    }

    const realTarget = fs.realpathSync(targetPath);
    const realRoot = fs.realpathSync(repoRoot);
    const relativeRealTarget = path.relative(realRoot, realTarget);
    if (relativeRealTarget.startsWith("..") || path.isAbsolute(relativeRealTarget)) {
      fail(`Refusing to remove path outside repo after realpath: ${realTarget}`);
    }
  }
}

assertSafeLocalContext();
assertSafeTarget();

const runningNextDev = inspectRunningNextDev();
if (runningNextDev === null && !dryRun) {
  fail("Could not inspect running processes. Refusing to remove .next/dev.");
}

if (runningNextDev && !dryRun) {
  fail("Next dev appears to be running. Stop `npm run dev` before clearing .next/dev.");
}

if (!fs.existsSync(targetPath)) {
  writeLine(".next/dev is already absent.");
  process.exit(0);
}

printSize("before");
if (dryRun) {
  if (runningNextDev === null) {
    writeLine("Could not inspect running processes. A non-dry run would refuse to remove .next/dev.");
  }
  if (runningNextDev) {
    writeLine("Next dev appears to be running. A non-dry run would refuse to remove .next/dev.");
  }
  writeLine("Dry run only. .next/dev was not removed.");
  process.exit(0);
}

fs.rmSync(targetPath, { recursive: true, force: true });
writeLine("Removed .next/dev. It will be regenerated by the next dev/build run.");
printSize("after");
